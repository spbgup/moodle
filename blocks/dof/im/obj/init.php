    /** Получить url, ведущий на объект из storage
     * @param string $storage - код хранилища, в котором лежит объект, на который нужно получить ссылку
     * @param int    $objectid - id объекта в хранилище
     * @param string $action[optional] - действие, которое будет запущено при переходе по ссылке (просмотр, удаление, и т.п.)
     * @param array  $urlparams[optional] - дополнительные параметры для URL
     * 
     * @return string - ссылка на нужный объект
     */
    public function get_object_url($storage, $objectid, $action='view', array $urlparams=array())
    {
        $mixedvar = array();
        $mixedvar['storage']   = $storage;
        $mixedvar['action']    = $action;
        $mixedvar['urlparams'] = $urlparams;
        $url = '';
        
        // посылаем широковещательный запрос для получения ссылки
        $result = $this->dof->send_event($this->type(), $this->code(), 'get_object_url', $objectid, $mixedvar);
        
        if ( ! $result->raw() )
        {
            return array();
        }
        
        return $result;
    }
