    /** Получить список параметров для фунции has_hight()
     * @todo завести дополнительные права в плагине storage/persons и storage/contracts 
     * и при редактировании контракта или персоны обращаться к ним
     * 
     * @return object - список параметров для фунции has_hight()
     * @param string $action - совершаемое действие
     * @param int $objectid - id объекта над которым совершается действие
     * @param int $personid
     */
    protected function get_access_parametrs($action, $objectid=0, $personid)
    {
        $result = new object();
        $result->plugintype   = $this->type();
        $result->plugincode   = $this->code();
        $result->code         = $action;
        $result->personid     = $personid;
        $result->departmentid = optional_param('departmentid', 0, PARAM_INT);
        $result->objectid     = $objectid;
        
        return $result;
    }    

    /** Проверить права через плагин acl.
     * Функция вынесена сюда, чтобы постоянно не писать длинный вызов и не перечислять все аргументы
     * 
     * @return bool
     * @param object $acldata - объект с данными для функции storage/acl->has_right() 
     */
    protected function acl_check_access_paramenrs($acldata)
    {
        return $this->dof->storage('acl')->
                    has_right($acldata->plugintype, $acldata->plugincode, $acldata->code, 
                              $acldata->personid, $acldata->departmentid, $acldata->objectid);
    }    

    /** Задаем права доступа при помощи плагина storage/acl
     * 
     * @return array
     */
    public function acldefault()
    {
        $a = array();
        // право видеть все объекты из всех хранилищ. Может только root
        $a['view']         = array('roles'=>array());
        // право редактировать все объекты из всех хранилищ. Может только root
        $a['edit']         = array('roles'=>array());
        // право удалять все объекты из всех хранилищ. Может только root
        $a['delete']       = array('roles'=>array());
        // право сменять статус всем объектам из всех хранилищ. Может только root
        $a['changestatus'] = array('roles'=>array());
        
        return $a;
    }    
    
